"""
Escribe un programa que solicite al usuario un número entero y calcule su cuadrado y su cubo.

"""

numero = int(input("Ingrese un número: "))

cuadrado = numero **2
cubo = numero **3
print(f"El cuadrado del numero '{numero}' es: {cuadrado}")
print(f"El cubo del numero '{numero}' es: {cubo}")












